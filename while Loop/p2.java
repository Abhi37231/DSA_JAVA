import java.util.*;

public class p2 {
    public static void main(String[] args) {
        int counter = 1;
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter The number till you want to print");
        int n  = sc.nextInt();


        while (counter <= n){
            System.out.println(counter);
            counter++;

        }
    }
    
}
